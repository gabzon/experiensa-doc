<h2>Description</h2>
	Plan and sell transportation and accommodations for travel agency customers. Determine destination, modes of transportation, travel dates, costs, and accommodations required. May also describe, plan, and arrange itineraries and sell tour packages. May assist in resolving clients' travel problems.
<h2><?php _e('Common Tasks') ?></h2>
<ul>
    <li>Collect payment for transportation and accommodations from customer. Process sales or other transactions.</li>
    <li>Converse with customer to determine destination, mode of transportation, travel dates, financial considerations, and accommodations required. Gather customer or product information to determine customer needs.</li>
    <li>Compute cost of travel and accommodations, using calculator, computer, carrier tariff books, and hotel rate books, or quote package tour's costs. Calculate costs of goods or services.</li>
    <li>Book transportation and hotel reservations, using computer terminal or telephone. Prepare sales or other contracts.</li>
    <li>Plan, describe, arrange, and sell itinerary tour packages and promotional travel incentives offered by various travel carriers. Sell products or services.</li>
    <li>Provide customer with brochures and publications containing travel information, such as local customs, points of interest, or foreign country regulations. Distribute promotional literature or samples to customers.</li>
    <li>Print or request transportation carrier tickets, using computer printer system or system link to travel carrier.</li>
    <li>Investigate complaints regarding safety violations.</li>
    <li>Examine carrier operating rules, employee qualification guidelines, or carrier training and testing programs for compliance with regulations or safety standards.</li>
</ul>
